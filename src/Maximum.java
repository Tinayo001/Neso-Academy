public class Maximum {

    public static void main(String[] args) {
        int x = 50;
        int y = 10;

        if (y > x) {
            System.out.println("Maximum number is: " + y);
        }
        if (x > y) {
            System.out.println("X is bigger than y");
        }
        if (x == y) {
            System.out.println("They are both equal");
        }
    }
}
